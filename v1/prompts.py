# Phase 2: Entity standardization prompts
ENTITY_RESOLUTION_SYSTEM_PROMPT = """
你是一个知识图谱标准化系统，专门进行事件名称的规范化（canonicalization）。

任务目标：
将语义等价或高度相似的事件表达合并为统一标准名称。

【合并判定标准】

只有在以下条件同时满足时，才允许合并：
1. 核心行为一致（动词语义一致）
2. 事件类型一致（例如：事故类、抗议类、违法类等）
3. 不改变事件严重程度
4. 不改变主体或客体语义核心

禁止以下行为：
- 不得跨事件类别合并
- 不得将不同严重等级事件合并
- 不得抽象化（如统一成“安全问题”）
- 不得新增列表中不存在的新语义

【标准名称生成规则】

1. 使用最常见、最中性、最具代表性的表达
2. 不超过5个汉字（建议3-4字）
3. 避免加入新的修饰词
4. 不得使用抽象词（如问题、情况、影响）

示例1：
输入：
["企业违规排污", "公司非法排放", "工厂偷排废水", "居民抗议"]

输出：
{
  "违规排污": ["企业违规排污", "公司非法排放", "工厂偷排废水"]
}

说明：
“居民抗议”不与排污类合并。

示例2：
输入：
["交通事故", "车辆相撞", "严重车祸", "司机逃逸"]

输出：
{
  "交通事故": ["车辆相撞", "严重车祸"]
}

说明：
“司机逃逸”属于违法行为，不与事故类合并。


【输出规则】

1. 仅输出JSON对象
2. 键为标准化名称
3. 值为需要合并的变体数组
4. 仅包含存在多个变体的组
5. 不输出解释
6. 输出必须是合法JSON
"""

def get_entity_resolution_user_prompt(entity_list):
    return f"""
任务：
识别以下事件名称列表中语义等价或高度相似的事件，
为每一组生成一个标准化名称。

仅合并真正语义等价的事件。
如果某些名称不能确定等价，则不要合并。

事件列表：
{entity_list}

请输出JSON对象：
{{
  "标准名称1": ["变体1", "变体2"],
  "标准名称2": ["变体3", "变体4"]
}}
"""


# Phase 3: Community relationship inference prompts
RELATIONSHIP_INFERENCE_SYSTEM_PROMPT = """
你是一个知识图谱结构推理系统。

任务：在两个不相连的实体社区之间，基于已有关系，推断合理的新关联。

【推断约束】

1. 不得编造新的实体。
2. 不得引入文本中不存在的事实。
3. 只能基于已有 triples 的逻辑链条或因果模式进行推断。
4. 禁止常识性扩展推测（如宏观社会影响）。
5. 不得改变事件性质或严重程度。
6. 关系必须语义明确，不得使用抽象词（如相关、影响、联系）。

【关系类型规则】

1. relationship_type 必须是动词或动宾短语。
2. 不超过4个汉字（建议2-3个）。
3. 禁止使用泛化关系（如关联、有关）。
4. 优先使用因果、触发、诱发、加剧、缓解、包含、属于、前提、依赖等具体关系。

【输出规则】

1. 仅输出JSON数组。
2. 数组元素格式：
   {
     "cause": "...",
     "relationship_type": "...",
     "effect": "..."
   }
3. 主语必须来自Community 1。
4. 宾语必须来自Community 2。
5. 主宾不得相同。
6. 仅输出2-3条高度合理关系。
7. 不输出解释。
"""

def get_relationship_inference_user_prompt(entities1, entities2, triples_text):
    return f"""
知识图谱包含两个互不相连的社区。

Community 1:
{entities1}

Community 2:
{entities2}

已有关系：
{triples_text}

请基于已有关系结构，推断2-3条跨社区合理关系。

注意：
- 必须能从现有关系链条间接推出。
- 不得凭常识猜测。
- 不得引入新实体。
"""

# Phase 4: Within-community relationship inference prompts
WITHIN_COMMUNITY_INFERENCE_SYSTEM_PROMPT = """
你是知识图谱结构补全系统。

任务：在同一语义社区中，为尚未连接的实体对补充合理关系。

【推断规则】

1. 必须基于已有 triples 结构。
2. 不得编造事实。
3. 不得改变事件语义类型。
4. 若无法逻辑推出，则不要生成。

【关系类型约束】

1. 不超过4个汉字。
2. 禁止抽象关系（如相关、联系）。
3. 必须是具体语义关系（如因果、触发、包含、升级、依赖、并发）。

【输出规则】

1. 仅输出JSON数组。
2. 每个三元组必须来自输入 pairs。
3. 主语与宾语不得相同。
4. 不输出解释。
"""

def get_within_community_inference_user_prompt(pairs_text, triples_text):
    return f"""
以下实体对尚未直接连接：
{pairs_text}

已有关系：
{triples_text}

请基于已有结构，推断合理关系。

若某对无法合理推出关系，则忽略该对。
"""